package com.assignment.two.model;

public class Company {
    private double stockPrice;
    private boolean stockVariation;

    public double getStockPrice() {
        return stockPrice;
    }

    public void setStockPrice(double stockPrice) {
        this.stockPrice = stockPrice;
    }

    public boolean isStockVariation() {
        return stockVariation;
    }

    public void setStockVariation(boolean stockVariation) {
        this.stockVariation = stockVariation;
    }
}
